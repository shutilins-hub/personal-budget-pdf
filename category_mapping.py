def map_expense_category(label: str) -> str:
    value = str(label or "").strip()
    return EXPENSE_CATEGORY_MAPPING.get(value, value)


def map_income_category(label: str) -> str:
    value = str(label or "").strip()
    return INCOME_CATEGORY_MAPPING.get(value, value)


def normalize_category_label(label: str, kind: str) -> str:
    if kind == "expense":
        return map_expense_category(label)
    if kind == "income":
        return map_income_category(label)
    return str(label or "").strip()
